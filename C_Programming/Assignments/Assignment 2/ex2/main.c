#include <stdio.h>

void main ()
{
	//user enter an alphabet
	char c ;
	printf("Enter an alphabet: ");
	fflush(stdout);
	scanf("%c", &c);

	//check whether the alphabet is vowel or consonant
	switch (c)
	{
		case 'A':
		case 'a':
		case 'E':
		case 'e':
		case 'I':
		case 'i':
		case 'O':
		case 'o':
		case 'U':
		case 'u':
		{
			printf("%c os a vowel", c);
		}
		break;
		default:
		{
			printf("%c is a consonant", c);
		}
		break;
		}
}
